public class ReportRuleConfigurationException extends Exception {

    public ReportRuleConfigurationException() {
        super();
    }

    public ReportRuleConfigurationException(String message) {
        super(message);
    }

    public ReportRuleConfigurationException(Throwable cause) {
        super(cause);
    }

    public ReportRuleConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }
}
