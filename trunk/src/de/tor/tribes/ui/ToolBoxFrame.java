class HideThread extends Thread {

    JFrame mParent;
    boolean hidden = false;

    public HideThread(JFrame pParent) {
        mParent = pParent;
        setDaemon(true);
    }

    public void setHidden(boolean v) {
        hidden = v;
    }

    public void run() {
        while (true) {
            if (hidden) {
                if (mParent.getHeight() > 20) {
                    mParent.setSize(mParent.getWidth(), mParent.getHeight() - 10);
                }
            } else {
                if (mParent.getHeight() < 200) {
                    mParent.setSize(mParent.getWidth(), mParent.getHeight() + 10);
                }
            }

            try {
                Thread.sleep(100);
            } catch (Exception e) {
            }
        }
    }
}
