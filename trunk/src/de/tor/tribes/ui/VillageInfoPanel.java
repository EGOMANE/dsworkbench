    private void fireShowAllyInfoEvent(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_fireShowAllyInfoEvent
        if(mVillage == null) return;
        Tribe t = mHolder.getTribes().get(mVillage.getTribe());
        if(t == null){
            jVillagePlayer.setText("Verlassen");
            jVillageTribe.setText("Keinen");
        }else{
            jVillagePlayer.setText(t.getName());
            Ally a = mHolder.getAllies().get(t.getAlly());
            if(a == null){
                jVillageTribe.setText("Keinen");
            }else{
                jVillageTribe.setText(a.getName());
            }
        }
}//GEN-LAST:event_fireShowAllyInfoEvent
    
private void fireShowPlayerInfoEvent(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_fireShowPlayerInfoEvent
    
    if(mVillage == null) return;
    Tribe t = mHolder.getTribes().get(mVillage.getTribe());
    if(t == null){
        //no player
        return;
    }else{
        final JFrame f = new JFrame();
        f.setTitle("Spieler " + t.getName());
        PlayerInfoPanel pinfo = new PlayerInfoPanel(mHolder);
        pinfo.showPlayer(t.getId());
        f.add(pinfo);
        f.setLocationRelativeTo(this);
        f.setLocation(getMousePosition());
        f.pack();
        f.setVisible(true);
    }
}//GEN-LAST:event_fireShowPlayerInfoEvent


