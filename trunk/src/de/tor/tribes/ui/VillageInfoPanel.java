    public void showVillage(int pX, int pY){
        mVillage = mHolder.getVillages()[pX][pY];
        if(mVillage == null) return;
        jVillageName.setText(mVillage.getName());
        String pos = Integer.toString(mVillage.getX()) + "|" + Integer.toString(mVillage.getY());
        jVillagePos.setText(pos);
        jVillagePoints.setText(Integer.toString(mVillage.getPoints()));
        
        Tribe t = mHolder.getTribes().get(mVillage.getTribe());
        if(t == null){
            jVillagePlayer.setText("Verlassen");
            jVillageTribe.setText("Keinen");
        }else{
            jVillagePlayer.setText(t.getName());
            Ally a = mHolder.getAllies().get(t.getAlly());
            if(a == null){
                jVillageTribe.setText("Keinen");
            }else{
                jVillageTribe.setText(a.getName());
            }
        }
    }
    
